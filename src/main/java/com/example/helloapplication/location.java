package com.example.helloapplication;

public class location implements serializable{
    private static final long serialVersionUID = 41L;

    private double x;
    private double y;

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
